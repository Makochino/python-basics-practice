from typing import List

class Solution:
    def containsDuplicate(self, nums: List[int]) -> bool:
        # 1. Создаем пустую хэш-таблицу (словарь) для хранения просмотренных чисел
        # В Python для этой цели, когда нам нужно просто хранить наличие,
        # часто используют 'set' (множество), так как оно оптимизировано
        # для быстрого поиска наличия и не хранит значения.
        # Но для демонстрации работы словаря, мы можем использовать его.
        # В этом случае, значением может быть что угодно, например, True, или индекс.
        # Здесь мы будем использовать число как ключ, а его индекс как значение (хотя индекс нам тут не нужен, просто для примера словаря).
        seen_numbers = {} # Или new_set = set() для более идиоматичного решения с множеством

        # 2. Итерируем по каждому числу в списке nums
        # Нам не нужны индексы для этой конкретной задачи,
        # поэтому можно просто использовать for num in nums:
        for num in nums:
            # 3. На каждом шаге проверяем, содержится ли текущее число в нашей хэш-таблице
            if num in seen_numbers:
                # 4. Если число уже есть в хэш-таблице, это означает, что мы нашли дубликат
                return True # Возвращаем True, потому что дубликат найден

            # 5. Если числа нет в хэш-таблице, добавляем его туда
            # Это делается, чтобы "запомнить" это число на будущее.
            # Если мы используем словарь, значением может быть что угодно, например True,
            # или можно было бы использовать индекс, как в Two Sum.
            # Для Contains Duplicate нам достаточно просто хранить сам элемент.
            seen_numbers[num] = True # Просто помечаем, что число было "видено"

        # 6. Если цикл завершился, и мы не вернули True, значит, дубликатов не было
        return False

# --- Примеры использования ---
sol = Solution()

# Пример 1: С дубликатами
nums1 = [1, 2, 3, 1]
print(f"Список: {nums1}, Содержит дубликаты? {sol.containsDuplicate(nums1)}") # Ожидаем True

# Пример 2: Без дубликатов
nums2 = [1, 2, 3, 4]
print(f"Список: {nums2}, Содержит дубликаты? {sol.containsDuplicate(nums2)}") # Ожидаем False

# Пример 3: С несколькими дубликатами
nums3 = [1, 1, 1, 3, 3, 4, 3, 2, 4, 2]
print(f"Список: {nums3}, Содержит дубликаты? {sol.containsDuplicate(nums3)}") # Ожидаем True

# Пример 4: Пустой список
nums4 = []
print(f"Список: {nums4}, Содержит дубликаты? {sol.containsDuplicate(nums4)}") # Ожидаем False

# Пример 5: Список из одного элемента
nums5 = [7]
print(f"Список: {nums5}, Содержит дубликаты? {sol.containsDuplicate(nums5)}") # Ожидаем False